import random

from faker import Faker

# 日期格式数组
date_formats = [
    # 标准格式 - 年月日，横杠分隔
    "%Y-%m-%d",  # 2024-01-01
    "%Y-%#m-%#d",  # 2024-1-1（不补零）

    # 标准格式 - 年月日，斜杠分隔
    "%Y/%m/%d",  # 2024/01/01
    "%Y/%#m/%#d",  # 2024/1/1

    # 标准格式 - 年月日，点分隔
    "%Y.%m.%d",  # 2024.01.01
    "%Y.%#m.%#d",  # 2024.1.1

    # 标准格式 - 年月日，无分隔符
    "%Y%m%d",  # 20240101

    # 短年份 - 两位数年份
    "%y-%m-%d",  # 24-01-01
    "%y/%m/%d",  # 24/01/01
    "%y.%m.%d",  # 24.01.01
    "%y%m%d",  # 240101

    # 中文格式 - 完整
    "%Y年%m月%d日",  # 2024年01月01日
    "%Y年%#m月%#d日",  # 2024年1月1日（不补零）

    # 欧洲格式 - 日月年
    "%d/%m/%Y",  # 01/01/2024
    "%d-%m-%Y",  # 01-01-2024
    "%d.%m.%Y",  # 01.01.2024
    "%#d/%#m/%Y",  # 1/1/2024
    "%#d-%#m-%Y",  # 1-1-2024

    # 美式格式 - 月日年
    "%m/%d/%Y",  # 01/01/2024
    "%m-%d-%Y",  # 01-01-2024
    "%m.%d.%Y",  # 01.01.2024
    "%#m/%#d/%Y",  # 1/1/2024
    "%#m-%#d-%Y",  # 1-1-2024

    # 短年份 - 欧洲格式
    "%d/%m/%y",  # 01/01/24
    "%d-%m-%y",  # 01-01-24

    # 短年份 - 美式格式
    "%m/%d/%y",  # 01/01/24
    "%m-%d-%y",  # 01-01-24

    # 英文月份 - 缩写，带逗号
    "%b %d, %Y",  # Jan 01, 2024
    "%B %d, %Y",  # January 01, 2024

    # 英文月份 - 缩写，无逗号
    "%b %d %Y",  # Jan 01 2024
    "%B %d %Y",  # January 01 2024

    # 英文月份 - 日在前
    "%d %b %Y",  # 01 Jan 2024
    "%d %B %Y",  # 01 January 2024
    "%#d %b %Y",  # 1 Jan 2024（日不补零）
    "%#d %B %Y",  # 1 January 2024

    # 英文月份 - 短年份
    "%b %d, %y",  # Jan 01, 24
    "%d %b %y",  # 01 Jan 24

    # 英文月份 - 短年份（日不补零）
    "%b %#d, %y",  # Jan 01, 24
    "%#d %b %y",  # 01 Jan 24
]


def dates(num: int) -> list[str]:
    faker = Faker("zh_CN")
    date_format = random.choice(date_formats)
    samples = []

    for _ in range(num):
        samples.append(faker.date_time().strftime(date_format))

    return samples
