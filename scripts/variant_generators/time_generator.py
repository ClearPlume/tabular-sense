import random

from faker import Faker

# 时间格式变体列表
time_formats = [
    # 24小时制 - 标准格式，带秒
    "%H:%M:%S",  # 14:30:45
    "%H.%M.%S",  # 14.30.45

    # 24小时制 - 标准格式，不带秒
    "%H:%M",  # 14:30
    "%H.%M",  # 14.30

    # 24小时制 - 中文"时"系列
    "%H时%M分%S秒",  # 14时30分45秒
    "%H时%M分",  # 14时30分

    # 24小时制 - 中文"点"系列
    "%H点%M分%S秒",  # 14点30分45秒
    "%H点%M分",  # 14点30分

    # 12小时制 - 英文，%p在后
    "%I:%M:%S %p",  # 02:30:45 PM（有空格）
    "%I:%M:%S%p",  # 02:30:45PM（无空格）
    "%I:%M %p",  # 02:30 PM
    "%I:%M%p",  # 02:30PM
    "%I.%M.%S %p",  # 02.30.45 PM
    "%I.%M.%S%p",  # 02.30.45PM

    # 12小时制 - 中文，%p在前
    "%p%I时%M分%S秒",  # 下午02时30分45秒（无空格）
    "%p %I时%M分%S秒",  # 下午 02时30分45秒（有空格）
    "%p%I时%M分",  # 下午02时30分
    "%p %I时%M分",  # 下午 02时30分
    "%p%I点%M分%S秒",  # 下午02点30分45秒
    "%p %I点%M分%S秒",  # 下午 02点30分45秒
    "%p%I点%M分",  # 下午02点30分
    "%p %I点%M分",  # 下午 02点30分

    # 12小时制 - 中文，%p在后
    "%I时%M分%S秒%p",  # 02时30分45秒下午（无空格）
    "%I时%M分%S秒 %p",  # 02时30分45秒 下午（有空格）
    "%I时%M分%p",  # 02时30分下午
    "%I时%M分 %p",  # 02时30分 下午
    "%I点%M分%p",  # 02点30分下午
    "%I点%M分 %p",  # 02点30分 下午

    # 紧凑格式
    "%H%M%S",  # 143045
    "%H%M",  # 1430

    # 不补零格式
    "%#H:%#M:%#S",  # 14:30:5
    "%#H:%#M",  # 14:30
]


def times(num: int) -> list[str]:
    faker = Faker("zh_CN")
    time_format = random.choice(time_formats)
    samples = []

    for _ in range(num):
        samples.append(faker.date_time().strftime(time_format))

    return samples
